                            <h6>We work on:</h6>
                            <div class="container-xxl text-justify">
                                <ul>
                                    <li>Fighting Stigma and Isolation
We work to combat the social stigma and exclusion faced by people living with mental illness, especially in rural communities.</li>
                                    <li>Promoting Mental Health Awareness
We organize seminars and awareness campaigns in collaboration with mental health professionals to foster positive attitudes.</li>
                                    <li>Supporting Families
We help families live better with their loved ones affected by mental illness, aiming to improve their everyday lives.</li>
                                    <li>Protecting Patients’ Rights
We raise awareness about the rights of people with mental illness—many are still rejected, mistreated, or separated from their families.</li>
                                    <li>Building Local Capacity
We train psychology students as local therapists, encourage research on mental health, and create dialogue between patients, families, and recovered individuals.</li>
                                </ul>
